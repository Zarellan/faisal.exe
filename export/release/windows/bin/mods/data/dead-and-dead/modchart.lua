function onStepHit()
    if curStep == 4 then
        onBeatSaberNotes();
    end
        if curStep == 5 then
            onNormalNotes();

        end
        if curStep == 6 then
            onBeatSaberNotes();
        end
            if curStep == 7 then
                onNormalNotes();
    
            end
            if curStep == 16 then
                onBeatSaberNotes();
            end
            if curStep == 80 then
                onIDFK();
            end
            if curStep == 143 then
                onNormalNotesSmooth();
            end
            
    end 
    